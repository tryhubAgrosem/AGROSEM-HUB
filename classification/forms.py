def get_unique_values(key):
    values = list(
        Nomenclature.objects.distinct().order_by(key).exclude(
            Q(**{f'{key}__isnull': True}) | 
            Q(**{f'{key}__exact': ''})
        ).values_list(key, key)
    )
    print(f'Loaded {key}: {len(values)}')
    return [('', 'не обрано')] + values
    

